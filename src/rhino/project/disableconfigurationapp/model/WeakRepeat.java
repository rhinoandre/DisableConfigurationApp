package rhino.project.disableconfigurationapp.model;

import java.io.Serializable;

public class WeakRepeat implements Serializable {
	private static final long serialVersionUID = -9034184172658378700L;
	
	private String resource;
	private int number_of_weak;
	
	public WeakRepeat(String resource, int numberOfWeak) {
		this.resource = resource;
		this.number_of_weak = numberOfWeak;
	}
	
	public String getResource() {
		return resource;
	}
	public int getNumber_of_weak() {
		return number_of_weak;
	}
	
	@Override
	public String toString() {
		return resource;
	}
}
